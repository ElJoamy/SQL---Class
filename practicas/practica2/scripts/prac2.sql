-- Crear la base de datos si no existe
CREATE DATABASE IF NOT EXISTS CineDB;

-- Seleccionar la base de datos
USE CineDB;

-- Crear la tabla de Películas
CREATE TABLE IF NOT EXISTS Peliculas (
    PeliculaID INT AUTO_INCREMENT PRIMARY KEY,
    Titulo VARCHAR(100),
    Director VARCHAR(50),
    Genero VARCHAR(30),
    Duracion INT -- Duración en minutos
);

-- Crear la tabla de Salas
CREATE TABLE IF NOT EXISTS Salas (
    SalaID INT AUTO_INCREMENT PRIMARY KEY,
    NumeroSala INT,
    Capacidad INT
);

-- Crear la tabla de Proyecciones
CREATE TABLE IF NOT EXISTS Proyecciones (
    ProyeccionID INT AUTO_INCREMENT PRIMARY KEY,
    PeliculaID INT,
    SalaID INT,
    FechaHora DATETIME,
    FOREIGN KEY (PeliculaID) REFERENCES Peliculas(PeliculaID),
    FOREIGN KEY (SalaID) REFERENCES Salas(SalaID)
);

-- Crear la tabla de Clientes
CREATE TABLE IF NOT EXISTS Clientes (
    ClienteID INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(50),
    Apellido VARCHAR(50),
    Email VARCHAR(100)
);

-- Crear la tabla de Entradas
CREATE TABLE IF NOT EXISTS Entradas (
    EntradaID INT AUTO_INCREMENT PRIMARY KEY,
    ProyeccionID INT,
    ClienteID INT,
    NumeroAsiento INT,
    Precio DECIMAL(5,2),
    FOREIGN KEY (ProyeccionID) REFERENCES Proyecciones(ProyeccionID),
    FOREIGN KEY (ClienteID) REFERENCES Clientes(ClienteID)
);

